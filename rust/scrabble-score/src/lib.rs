pub fn score(word: &str) -> u32 {
    let letter_value = |c| match c {
        'A' | 'E' | 'I' | 'O' | 'U' | 'L' | 'N' | 'R' | 'S' | 'T' => 1,
        'D' | 'G' => 2,
        'B' | 'C' | 'M' | 'P' => 3,
        'F' | 'H' | 'V' | 'W' | 'Y' => 4,
        'K' => 5,
        'J' | 'X' => 8,
        'Q' | 'Z' => 10,
        _ => 0,
    };
    word.to_uppercase().chars()
        .map(letter_value)
        .fold(0, |sum, i| sum + i)
}
